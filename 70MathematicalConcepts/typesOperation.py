# Basic arithmetic operations are supported on integers and floats
a = 1
b = 2
print(a + b)
#> 3
print(a - b)
#> -1
print(a * b)
#> 2
print(a / b)
#> 0.5

# Integers and floats can be added together
c = 3.5
print(a - b + c)
#> 2.5

# Strings are also well-equipped
s = 'python'
print(s + s)
#> pythonpython
print(len(s))
#> 6
# Strings are lists of characters
print(s[:3])
#> pyt